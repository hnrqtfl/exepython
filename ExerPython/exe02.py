'''Faça um algoritmo para ler três números e imprimir a soma, média e produto dos números lidos.'''

n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))
n3 = int(input("Digiteo terceiro número "))

print("A soma dos números é: ", n1+n2+n3)
print("A média dos números é: ",(n1+n2+n3)/3)
print("O produto dos números acima é: ", n1*n2*n3)

